CREATE TABLE `users`(
  `id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主キー(PK)',
  `type` TINYINT(3) UNSIGNED NOT NULL COMMENT 'ユーザー種別(0:women, 1:men)',
  `name` VARCHAR(255) NOT NULL COMMENT 'ユーザー名',
  `line_user_id` VARCHAR(255) DEFAULT NULL COMMENT 'LINEのユーザーID',
  `firebase_user_id` VARCHAR(255) DEFAULT NULL COMMENT 'FirebaseのユーザーID',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '作成日時',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新日時',
  PRIMARY KEY (`id`),
  UNIQUE KEY `users_type_unique` (`type`)
) ENGINE=InnoDB;

CREATE TABLE `categories`(
  `id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主キー(PK)',
  `name` VARCHAR(255) NOT NULL COMMENT 'カテゴリ名',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '作成日時',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新日時',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB;

CREATE TABLE `payments`(
  `id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主キー(PK)',
  `user_id` INT(10) UNSIGNED NOT NULL COMMENT 'usersテーブルのID(PK)',
  `category_id` INT(10) UNSIGNED NOT NULL COMMENT 'categoriesテーブルのID(PK)',
  `price` INT(10) UNSIGNED NOT NULL COMMENT '価格',
  `date` DATE NOT NULL COMMENT '日付',
  `memo` VARCHAR(255) DEFAULT NULL COMMENT '価格',
  `created_by` INT(10) UNSIGNED NOT NULL COMMENT '作成者のusersテーブルのID(PK)',
  `updated_by` INT(10) UNSIGNED NOT NULL COMMENT '更新者のusersテーブルのID(PK)',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '作成日時',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新日時',
  PRIMARY KEY (`id`),
  KEY `payments_date_index` (`date`),
  CONSTRAINT `payments_user_id_foreign` FOREIGN KEY (`user_id`) REFERENCES `users` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
  CONSTRAINT `payments_category_id_foreign` FOREIGN KEY (`category_id`) REFERENCES `categories` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;
